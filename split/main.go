package main

import "fmt"

// Split принимает строку s и разделитель sep, возвращая массив строк
func Split(s, sep string) []string {
	var result []string

	// Узнаем длину строки и длину разделителя
	sepLen := len(sep)
	sLen := len(s)

	// Защита от дурака: если разделитель пустой, резать не по чему
	if sepLen == 0 {
		return []string{s}
	}

	start := 0

	// Запускаем цикл. Он крутится, пока до конца строки остается
	// достаточно символов, чтобы туда мог поместиться разделитель.
	for i := 0; i <= sLen-sepLen; {

		// Берем "срез" строки текущего размера разделителя и сравниваем
		if s[i:i+sepLen] == sep {
			// Нашли! Отрезаем кусок от start до i и кладем в массив
			result = append(result, s[start:i])

			// Перепрыгиваем через длину самого разделителя
			i += sepLen
			// Ставим метку начала следующего слова на новую позицию
			start = i
		} else {
			// Если совпадения нет, просто делаем шаг вперед на 1 символ
			i++
		}
	}

	// Цикл закончился, но у нас остался последний кусок текста
	// после последнего разделителя. Добавляем его в массив.
	result = append(result, s[start:])

	return result
}

func main() {
	// Тестовые данные из инструкции
	s := "HelloHAhowHAareHAyou?" //

	// Форматирование %#v выведет результат в виде точного синтаксиса Go
	fmt.Printf("%#v\n", Split(s, "HA")) //
}
