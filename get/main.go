package main

import (
	"fmt"
	"strings"
)

// Get возвращает значение из вложенной структуры по указанному пути
// Мы используем interface{} (любой тип), так как значения в объекте могут быть какими угодно.
func Get(src interface{}, path string) interface{} { //
	// 1. Разбиваем строку пути на отдельные ключи по точке
	keys := strings.Split(path, ".")

	// 2. Создаем курсор, который будет "спускаться" вглубь объекта.
	// Сначала он указывает на сам исходный объект.
	var current interface{} = src

	// 3. Запускаем цикл по нашим ключам-инструкциям
	for _, key := range keys {
		// Главная магия Go (Утверждение типа / Type Assertion):
		// Мы проверяем, является ли текущий "current" картой (map),
		// у которой ключи — строки, а значения — что угодно (interface{}).
		if m, ok := current.(map[string]interface{}); ok {

			// Если это карта, проверяем, есть ли в ней нужный нам ключ
			if val, exists := m[key]; exists {
				// Если есть, наш курсор берет это значение и мы идем на следующий круг
				current = val
			} else {
				// Если ключа нет, путь неверный. Возвращаем пустоту.
				return nil
			}
		} else {
			// Если current — это не карта (а, например, просто строка или число),
			// значит мы не можем спуститься глубже. Путь неверный.
			return nil
		}
	}

	// 4. Если мы успешно прошли по всем ключам, возвращаем то, что у нас в руках
	return current //
}

func main() {
	// Воссоздаем JS-объект из инструкции средствами Go: { nested: { key: 'peekaboo' } }
	// map[string]interface{} — это карта, где ключи это строки, а значения — абсолютно любые типы данных.
	src := map[string]interface{}{ //
		"nested": map[string]interface{}{ //
			"key": "peekaboo", //
		},
	}

	path := "nested.key" //

	// Должно вывести: peekaboo
	fmt.Println(Get(src, path)) //
}
