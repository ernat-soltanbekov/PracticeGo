package main

import "fmt"

// Вспомогательная функция для безопасного перевода нашей строки в число
func parseDigit(s string) (int, bool) {
	// Если это просто одна цифра (например, "7")
	if len(s) == 1 {
		if s[0] >= '0' && s[0] <= '9' {
			// Вычитаем код символа '0' из нашего символа, получая реальное число int
			return int(s[0] - '0'), true
		}
	}
	// Если это отрицательное однозначное число (например, "-4")
	if len(s) == 2 {
		if s[0] == '-' && s[1] >= '0' && s[1] <= '9' {
			// Делаем то же самое, но делаем результат отрицательным
			return -int(s[1] - '0'), true
		}
	}
	// Если формат нарушен, возвращаем 0 и флаг ошибки (false)
	return 0, false
}

// Sum принимает две строки и возвращает их математическую сумму
func Sum(a, b string) int {
	// Конвертируем первую строку
	valA, okA := parseDigit(a)
	// Конвертируем вторую строку
	valB, okB := parseDigit(b)

	// Если хотя бы одна из строк не соответствует правилам (не цифра)
	if !okA || !okB {
		return 0 // Возвращаем 0, как указано в задании
	}

	// Если всё отлично, возвращаем сумму
	return valA + valB
}

func main() {
	// Тестовые данные из инструкции
	fmt.Println(Sum("1", "2"))  // Выведет 3
	fmt.Println(Sum("-4", "0")) // Выведет -4
	fmt.Println(Sum("7", "-3")) // Выведет 4
}
