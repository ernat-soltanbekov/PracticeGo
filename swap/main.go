package main

import "fmt"

// Swap принимает два указателя на int и меняет их значения местами
func Swap(a *int, b *int) { //
	// Магия Go: меняем значения, лежащие по адресам a и b, за один шаг
	*a, *b = *b, *a
}

func main() {
	// Тестовые данные из инструкции
	a := 0 //
	b := 1 //

	// Передаем в функцию адреса переменных a и b с помощью амперсанда (&)
	Swap(&a, &b) //

	// Выводим результат
	fmt.Println(a) //
	fmt.Println(b) //
}
