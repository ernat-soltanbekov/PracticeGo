package main

import (
	"fmt"
)

func main() {
	// Ставим два указателя на начало и конец алфавита.
	// Одинарные кавычки означают тип rune (числовой код символа).
	first := 'a'
	last := 'z'

	// Запускаем цикл ровно на 13 итераций (26 букв / 2)
	for i := 0; i < 13; i++ {
		// %c — это спецификатор формата. Он говорит функции Printf: 
		// "Возьми переданную руну и напечатай её как букву".
		// Мы передаем два %c, потому что печатаем сразу две буквы без пробелов.
		fmt.Printf("%c%c", first, last)
		
		// Сдвигаем указатели навстречу друг другу
		first++
		last--
	}
	
	// В конце обязательно печатаем перенос строки, как требует задание
	fmt.Println()
}