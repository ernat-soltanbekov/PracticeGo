package main

import "fmt"

// IsPrime возвращает true, если число простое, и false в противном случае
func IsPrime(nb int) bool { //
	// Базовая проверка: 1 и отрицательные числа не являются простыми
	if nb <= 1 {
		return false //
	}

	// Оптимизированный цикл: проверяем делители от 2 до квадратного корня из nb
	for i := 2; i*i <= nb; i++ {
		// Если число разделилось на i без остатка, значит у него есть делители, оно не простое
		if nb%i == 0 {
			return false
		}
	}

	// Если цикл закончился и мы не нашли ни одного делителя, число простое
	return true
}

func main() {
	// Тестовые данные из инструкции
	fmt.Println(IsPrime(5)) // Выведет: true
	fmt.Println(IsPrime(4)) // Выведет: false
}
