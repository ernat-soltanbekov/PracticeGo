package main

import "fmt"

func Sqrt(nb int) int {
	// 1. Проверка на отрицательные числа
	// В математике не бывает вещественных корней из отрицательных чисел
	if nb < 0 {
		return 0
	}

	// 2. Умный цикл перебора
	// Перебираем числа 'i', пока квадрат этого числа меньше или равен 'nb'
	for i := 0; i*i <= nb; i++ {
		// Если квадрат числа 'i' в точности совпал с 'nb' — мы нашли корень!
		if i*i == nb {
			return i // Выбрасываем результат и останавливаем функцию
		}
	}

	// 3. Если цикл завершился (мы "перелетели" число), а корень не найден
	return 0
}

func main() {
	// Тесты из твоего README
	fmt.Println(Sqrt(4)) // Ожидаем 2
	fmt.Println(Sqrt(3)) // Ожидаем 0 (так как корень дробный)

	// Дополнительные тесты для проверки краевых случаев
	fmt.Println(Sqrt(9))  // Ожидаем 3
	fmt.Println(Sqrt(0))  // Ожидаем 0
	fmt.Println(Sqrt(-5)) // Ожидаем 0
}
