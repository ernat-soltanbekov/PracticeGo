package main

import (
	"fmt"
	"os"
	"strconv"
)

func main() {
	// 1. Проверка количества аргументов
	// os.Args[0] - это название программы. Нам нужны еще 3 аргумента.
	if len(os.Args) != 4 {
		return // Завершаем программу молча
	}

	// 2. Конвертация текста в 64-битные числа с проверкой на ошибки
	v1, err1 := strconv.ParseInt(os.Args[1], 10, 64)
	v2, err2 := strconv.ParseInt(os.Args[3], 10, 64)

	// Если хотя бы одно значение - это текст (как "hello" из примера)
	// или слишком гигантское число (уже не влезает в память), err не будет равен nil
	if err1 != nil || err2 != nil {
		return
	}

	operator := os.Args[2]

	// 3. Выбор операции и ручная проверка на переполнение
	switch operator {
	case "+":
		res := v1 + v2
		// Переполнение сложения: если мы сложили два плюса, а получили минус,
		// или сложили два минуса, а получили плюс
		if (v2 > 0 && res < v1) || (v2 < 0 && res > v1) {
			return
		}
		fmt.Println(res)

	case "-":
		res := v1 - v2
		// Переполнение вычитания: логика обратная сложению
		if (v2 < 0 && res < v1) || (v2 > 0 && res > v1) {
			return
		}
		fmt.Println(res)

	case "*":
		if v1 == 0 || v2 == 0 {
			fmt.Println(0)
			return
		}
		res := v1 * v2
		// Умная проверка умножения: если поделить результат обратно на v1,
		// мы должны получить точное исходное v2. Если нет — было переполнение.
		if res/v1 != v2 {
			return
		}
		fmt.Println(res)

	case "/":
		if v2 == 0 {
			fmt.Println("No division by 0")
			return
		}
		// Защита от единственного критического сбоя при делении в 64-битных системах.
		// Деление самого минимального числа на -1 дает число больше максимального.
		if v1 == -9223372036854775808 && v2 == -1 {
			return
		}
		fmt.Println(v1 / v2)

	case "%":
		if v2 == 0 {
			fmt.Println("No modulo by 0")
			return
		}
		// Защита от того же математического парадокса, что и при делении
		if v1 == -9223372036854775808 && v2 == -1 {
			return
		}
		fmt.Println(v1 % v2)

	default:
		// Если передан неизвестный оператор (например, "p" из примера)
		return
	}
}
